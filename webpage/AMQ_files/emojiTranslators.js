function translateShortcodeToUnicode (text) {
	let shortcodeRegex = /(:[^:]*(?=:))/g;
	let shortcodesFound = {};
	let match;
	do {
		match = shortcodeRegex.exec(text);
		if (match) {
			shortcodesFound[match[1] + ':'] = true;
		}
	} while (match);

	Object.keys(shortcodesFound).forEach((shortcode) => {
		let unicodes = EMOJI_SHORTCODE_MAP[shortcode];
		if(unicodes){
			let unicodeString = "";
			unicodes.forEach((unicode) => {
				unicodeString += String.fromCodePoint(unicode);
			});
			text = text.replace(new RegExp(shortcode, 'g'), unicodeString);
		}
	});

	return text;
}

